    def primeSubOperation(self, nums: list[int]) -> bool:
        nums = [0] + nums
        for i in range(1, len(nums)):
            target = nums[i] - nums[i - 1]
            p = bisect_left(self.PRIMES, target)

            if p == len(self.PRIMES) or self.PRIMES[p] >= target:
                p = max(0, p - 1)

            if p > 0 or nums[i - 1] < nums[i] - self.PRIMES[p]:
                nums[i] -= self.PRIMES[p]
                continue

            if p == 0 and nums[i - 1] >= nums[i]:
                return False

        return True
